def download_file(url, dest_path):
    try:
        temp_path = dest_path + ".tmp"
        response = requests.get(url, stream=True, timeout=15, verify=False)
        response.raise_for_status()
        
        file_exists = os.path.exists(dest_path)
        if file_exists:
            try:
                existing_size = os.path.getsize(dest_path)
            except OSError:
                existing_size = 0
        else:
            existing_size = 0
            
        new_size = int(response.headers.get('content-length', 0))
        
        if file_exists and existing_size == new_size:
            print(f"  ‚úì –ê–∫—Ç—É–∞–ª—å–Ω–∞—è –≤–µ—Ä—Å–∏—è ({new_size//1024} –ö–ë)")
            return True
        
        with open(temp_path, 'wb') as f:
            for chunk in response.iter_content(chunk_size=8192):
                if chunk:
                    f.write(chunk)
        
        if file_exists:
            os.remove(dest_path)
        os.rename(temp_path, dest_path)
        
        if file_exists:
            print(f"  ‚úì –û–±–Ω–æ–≤–ª–µ–Ω ({new_size//1024} –ö–ë)")
        else:
            print(f"  ‚úì –ó–∞–≥—Ä—É–∂–µ–Ω ({new_size//1024} –ö–ë)")
        
        return True
        
    except Exception as e:
        log_error(f"–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Ñ–∞–π–ª–∞ {os.path.basename(dest_path)}", e)
        if os.path.exists(temp_path):
            try:
                os.remove(temp_path)
            except:
                pass
        return False

